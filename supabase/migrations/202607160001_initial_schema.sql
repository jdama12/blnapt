create policy "own profile update" on public.profiles for update to authenticated using (id = auth.uid()) with check (id = auth.uid());
create policy "admin profiles update" on public.profiles for update to authenticated using (public.is_admin()) with check (public.is_admin());
create policy "complaints read" on public.complaints for select to authenticated using (author_id = auth.uid() or public.is_admin());
create policy "complaints insert" on public.complaints for insert to authenticated with check ((author_id = auth.uid() and status = 'pending') or public.is_admin());
create policy "admin complaints update" on public.complaints for update to authenticated using (public.is_admin()) with check (public.is_admin());
create policy "comments read" on public.complaint_comments for select to authenticated using (exists(select 1 from public.complaints c where c.id = complaint_id and (c.author_id = auth.uid() or public.is_admin())));
