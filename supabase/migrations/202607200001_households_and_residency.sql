create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_household public.households%rowtype;
  phone_number text;
  last4 text;
begin
  phone_number := regexp_replace(coalesce(new.raw_user_meta_data->>'phone', ''), '[^0-9]', '', 'g');
  last4 := new.raw_user_meta_data->>'phone_last4';

  if phone_number !~ '^01[016789][0-9]{7,8}$' then
    raise exception '전화번호가 올바르지 않습니다.';
  end if;

  if last4 is null or last4 !~ '^[0-9]{4}$' then
    raise exception '전화번호 뒤 4자리가 올바르지 않습니다.';
  end if;

  select * into target_household
  from public.households
  where building = (new.raw_user_meta_data->>'building')::integer
    and unit = (new.raw_user_meta_data->>'unit')::integer;

  if not found then
    raise exception '등록되지 않은 세대입니다.';
  end if;

  insert into public.profiles(
    id, email, name, phone, building, unit,
    household_id, phone_last4, membership_status
  ) values (
    new.id,
    new.email,
    target_household.building || '동 ' || target_household.unit || '호 입주민',
    phone_number,
    target_household.building::text,
    target_household.unit::text,
    target_household.id,
    last4,
    'pending'
  );

  insert into public.registration_requests(
    applicant_id, household_id, phone, phone_last4, request_type, previous_resident_id
  ) values (
    new.id,
    target_household.id,
    phone_number,
    last4,
    case when target_household.current_resident_id is null then 'new' else 'replacement' end,
    target_household.current_resident_id
  );

  return new;
end;
$$;

