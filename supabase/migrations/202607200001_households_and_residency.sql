create or replace function public.approve_registration(request_id bigint)
returns table(approved_household_id bigint, resident_replaced boolean)
language plpgsql
security definer
set search_path = public
as $$
declare
  target_request public.registration_requests%rowtype;
  target_household public.households%rowtype;
  old_resident_id uuid;
begin
  if not public.is_admin() then
    raise exception '관리자만 가입을 승인할 수 있습니다.';
  end if;

  select * into target_request
  from public.registration_requests
  where id = request_id
  for update;

  if not found or target_request.status <> 'pending' then
    raise exception '처리할 수 없는 가입 요청입니다.';
  end if;

  select * into target_household
  from public.households
  where id = target_request.household_id
  for update;

  old_resident_id := target_household.current_resident_id;

  update public.profiles
  set approved = true,
      membership_status = 'active',
      replaced_at = null
  where id = target_request.applicant_id;

  if old_resident_id is not null and old_resident_id <> target_request.applicant_id then
    update public.profiles
    set approved = false,
        membership_status = 'replaced',
        replaced_at = now()
    where id = old_resident_id;
  end if;

  update public.households
  set current_resident_id = target_request.applicant_id
  where id = target_request.household_id;

  update public.registration_requests
  set status = 'approved',
      request_type = case when old_resident_id is null then 'new' else 'replacement' end,
      previous_resident_id = old_resident_id,
      reviewed_by = auth.uid(),
      reviewed_at = now()
  where id = target_request.id;

  update public.registration_requests
  set request_type = 'replacement',
      previous_resident_id = target_request.applicant_id
  where household_id = target_request.household_id
    and status = 'pending'
    and id <> target_request.id;

  return query select target_request.household_id, old_resident_id is not null;
end;
$$;

create or replace function public.reject_registration(request_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_applicant_id uuid;
begin
  if not public.is_admin() then
    raise exception '관리자만 가입을 거절할 수 있습니다.';
  end if;

  update public.registration_requests
  set status = 'rejected', reviewed_by = auth.uid(), reviewed_at = now()
  where id = request_id and status = 'pending'
  returning applicant_id into target_applicant_id;

  if target_applicant_id is null then
    raise exception '처리할 수 없는 가입 요청입니다.';
  end if;

  update public.profiles
  set approved = false, membership_status = 'rejected'
  where id = target_applicant_id;
end;
$$;

create or replace function public.bootstrap_first_admin(
  target_building integer,
  target_unit integer,
  target_phone_last4 text
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  target_request public.registration_requests%rowtype;
begin
  if session_user <> 'postgres' then
    raise exception 'SQL Editor의 postgres 역할에서만 실행할 수 있습니다.';
  end if;

  if exists(select 1 from public.profiles where role = 'admin' and approved = true and membership_status = 'active') then
    raise exception '이미 활성 관리자가 존재합니다.';
  end if;

  select r.* into target_request
  from public.registration_requests r
  join public.households h on h.id = r.household_id
  where h.building = target_building
    and h.unit = target_unit
    and r.phone_last4 = target_phone_last4
    and r.status = 'pending'
  order by r.created_at desc
  limit 1
  for update of r;

  if not found then
    raise exception '일치하는 가입 요청이 없습니다.';
  end if;

  update public.profiles
  set role = 'admin', approved = true, membership_status = 'active', replaced_at = null
  where id = target_request.applicant_id;

  update public.households
  set current_resident_id = target_request.applicant_id
  where id = target_request.household_id;

  update public.registration_requests
  set status = 'approved', reviewed_at = now()
  where id = target_request.id;

  return target_request.applicant_id;
end;
$$;

